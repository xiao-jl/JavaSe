package xuexi.xiao.kongzhi;

// for-练习01

// for练习-获取数据1-10和10-1

public class ForLianXi01 {
    public static void main(String[] args) {

        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }

        System.out.println("===============================");

        for (int ii = 10; ii >= 1; ii--) {
            System.out.println(ii);
        }

    }
}
