package xuexi.xiao.yinyongshujuleixing;

public class CeShiLei {

    private final String a;
    private final int b;


    public CeShiLei(String a, int b) {
        this.a = a;
        this.b = b;
    }

    public String getA() {
        return a;
    }
    public int getB() {
        return b;
    }

}
