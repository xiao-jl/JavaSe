package xuexi.xiao.yinyongshujuleixing;

public class CeShiLei {

    private String a;
    private int b;

    public CeShiLei() {

    }

    public CeShiLei(String a, int b) {
        this.a = a;
        this.b = b;
    }

    public String getA() {
        return a;
    }
    public void setA(String a) {
        this.a = a;
    }

    public int getB() {
        return b;
    }
    public void setB(int b) {
        this.b = b;
    }

}
